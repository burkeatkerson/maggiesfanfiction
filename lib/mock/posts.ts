/** Fill a full Post from a partial, applying sensible defaults. */
function makePost(p: Partial<Post> & Pick<Post, "title" | "slug">): Post {
  return {
    id: p.id ?? p.slug,
    title: p.title,
    headline: p.headline ?? null,
    slug: p.slug,
    excerpt: p.excerpt ?? null,
    content: p.content ?? null,
    body_font: p.body_font ?? "Crimson Text",
    category_id: p.category_id ?? null,
    series_id: p.series_id ?? null,
    series_part: p.series_part ?? null,
    tags: p.tags ?? [],
    status: p.status ?? "draft",
    featured_image_url: p.featured_image_url ?? null,
    featured_image_caption: p.featured_image_caption ?? null,
    seo_description: p.seo_description ?? null,
    reading_time: p.reading_time ?? 1,
    author_id: p.author_id ?? AUTHOR_ID,
    published_at: p.published_at ?? null,
    created_at: p.created_at ?? p.published_at ?? "2024-01-01T00:00:00Z",
    updated_at: p.updated_at ?? p.published_at ?? "2024-01-01T00:00:00Z",
  };
}

